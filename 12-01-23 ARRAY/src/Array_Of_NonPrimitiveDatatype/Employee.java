package Array_Of_NonPrimitiveDatatype;

public class Employee {
	int eid;
	String ename;
	double esal;
	double  eweight;
	double eheight;
	long emobno;
	String address;
	public int getEid() {
		return eid;
	}
	public void setEid(int eid) {
		this.eid = eid;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public double getEsal() {
		return esal;
	}
	public void setEsal(double esal) {
		this.esal = esal;
	}
	public double getEweight() {
		return eweight;
	}
	public void setEweight(double eweight) {
		this.eweight = eweight;
	}
	public double getEheight() {
		return eheight;
	}
	public void setEheight(double eheight) {
		this.eheight = eheight;
	}
	public long getEmobno() {
		return emobno;
	}
	public void setEmobno(long emobno) {
		this.emobno = emobno;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}

}
